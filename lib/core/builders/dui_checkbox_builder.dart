import 'package:digia_ui/components/checkbox/dui_checkbox.dart';
import 'package:digia_ui/components/checkbox/dui_checkbox_props.dart';
import 'package:digia_ui/core/json_widget_builder.dart';
import 'package:digia_ui/core/page/props/dui_widget_json_data.dart';
import 'package:flutter/material.dart';

class DUICheckboxBuilder extends DUIWidgetBuilder {
  DUICheckboxBuilder({required super.data});

  static DUICheckboxBuilder create(DUIWidgetJsonData data) {
    return DUICheckboxBuilder(data: data);
  }

  @override
  Widget build(BuildContext context) {
    return DUICheckbox(DUICheckboxProps.fromJson(data.props));
  }
}
